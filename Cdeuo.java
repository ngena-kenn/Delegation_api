package fr.sacem.api.model;

public class Cdeuo {
    private String codeUo;
    private String codeEtablissement;
    private String typeAgence;
    private String responsableUo;


    public String getCodeUo() {
        return codeUo;
    }

    public void setCodeUo(String codeUo) {
        this.codeUo = codeUo;
    }

    public String getCodeEtablissement() {
        return codeEtablissement;
    }

    public void setCodeEtablissement(String codeEtablissement) {
        this.codeEtablissement = codeEtablissement;
    }

    public String getTypeAgence() {
        return typeAgence;
    }

    public void setTypeAgence(String typeAgence) {
        this.typeAgence = typeAgence;
    }

    public String getResponsableUo() {
        return responsableUo;
    }

    public void setResponsableUo(String responsableUo) {
        this.responsableUo = responsableUo;
    }


}
